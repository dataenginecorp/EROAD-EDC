SELECT 
ID, 
NAME as SENSOR_NAME, 
DESCRIPTION as SENSOR_DESCRIPTION, 
CASE 
WHEN ID IN (2, 3, 9, 10, 11, 28, 43) THEN 'Core Telematics' 
WHEN ID IN (31, 54, 57, 58) THEN 'Electric Vehicle' 
WHEN ID IN (14, 18, 22, 32, 33, 34, 35, 36, 37, 41, 42, 44, 45, 47, 48, 50, 56, 59, 60, 72, 73) THEN 'Service' 
END AS ECM_SENSOR_TYPE_CATEGORY, 
IFNULL(UNITS,'') as SENSOR_UNITS 
FROM "${source_db}".core.sensor_datapoint 
WHERE ID IN (2, 3, 9, 10, 11, 28, 43, 31, 54, 57, 58, 14, 18, 22, 32, 33, 34, 35, 36, 37, 41, 42, 44, 45, 47, 48, 50, 56, 59, 60, 72, 73);